package com.sensores.modelo;
public class Alerta {
	private Celular idCelular;
	private int id;
	private EstadosEP idEstadoEp;
	
	public Celular getIdCelular() {
		return idCelular;
	}
	public void setIdCelular(Celular idCelular) {
		this.idCelular = idCelular;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public EstadosEP getIdEstadoEp() {
		return idEstadoEp;
	}
	public void setIdEstadoEp(EstadosEP idEstadoEp) {
		this.idEstadoEp = idEstadoEp;
	}
	
	
}
